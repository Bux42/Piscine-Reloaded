int		ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 2;
	while (sqrt * sqrt != nb && sqrt * 2 < nb)
	{
		sqrt++;
		if (sqrt * sqrt == nb)
			return (sqrt);
	}
	return (0);
}
